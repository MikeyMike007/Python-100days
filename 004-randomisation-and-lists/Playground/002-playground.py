myList = ["Delaware","Pennsylvania","New Jersey"]

# Printing list items

print(myList[0])
print(myList[1])
print(myList[2])

print(myList[-1])
print(myList[-2])
print(myList[-3])

# Updating list items

myList[0] = "New Delaware"

print(myList[0])

# Append a list

myList.append("Mikael Land")
print(myList)

# Extend a list

myList.extend(["Jenny land","Lily land", "Charlie land"])
print(myList)
